    public void cargarTabla(){

    modelo.setRowCount(0);

    for(Usuario u : login.listaUsuariosGlobal){

        String estadoTexto = "";

        if(u.estado == 1){
            estadoTexto = "Activo";
        }

        if(u.estado == 0){
            estadoTexto = "Inactivo";
        }

        if(u.estado == 2){
            estadoTexto = "Bloqueado";
        }

        modelo.addRow(new Object[]{
            u.usuario,
            u.rol,
            estadoTexto
        });
    }
}

