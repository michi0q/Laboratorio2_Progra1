    private void btnCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCrearActionPerformed
        
    String user = txtUsuario.getText();
    String pass = txtPassword.getText();
    String confirmar = txtConfirmar.getText();
    String rol = cmbRol.getSelectedItem().toString();

    if(user.isEmpty() || pass.isEmpty()){
        JOptionPane.showMessageDialog(this, "Complete todos los campos");
        return;
    }

    if(!pass.equals(confirmar)){
        JOptionPane.showMessageDialog(this, "Las contraseñas no coinciden");
        return;
    }

    if(pass.length() < 13){
        JOptionPane.showMessageDialog(this, "Mínimo 13 caracteres");
        return;
    }

    if(!pass.matches(".*[A-Z].*")){
        JOptionPane.showMessageDialog(this, "Debe tener una mayúscula");
        return;
    }

    if(!pass.matches(".*[!@#$%^&*()].*")){
        JOptionPane.showMessageDialog(this, "Debe tener símbolo especial");
        return;
    }

    int estado = 1;

    if(rbInactivo.isSelected()){
        estado = 0;
    }

    login.listaUsuariosGlobal.add(
        new Usuario(user, pass, rol, estado)
    );

    JOptionPane.showMessageDialog(this, "Usuario creado");

    cargarTabla();

    }//GEN-LAST:event_btnCrearActionPerformed

    private void btnGuardarCambiosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarCambiosActionPerformed
        // TODO add your handling code here:

    int fila = tablaUsuarios.getSelectedRow();

    if(fila >= 0){

        Usuario u = login.listaUsuariosGlobal.get(fila);

        u.rol = cmbRol.getSelectedItem().toString();

        if(rbActivo.isSelected()){
            u.estado = 1;
        }

        if(rbInactivo.isSelected()){
            u.estado = 0;
        }

        JOptionPane.showMessageDialog(this, "Usuario actualizado");

        cargarTabla();

    } else {
        JOptionPane.showMessageDialog(this, "Seleccione un usuario");
    }

    }//GEN-LAST:event_btnGuardarCambiosActionPerformed

